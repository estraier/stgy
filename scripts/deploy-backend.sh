INJECT_CFG_BACKEND_API_BASE_URL="$CFG_BACKEND_API_BASE_URL" \
INJECT_CFG_BACKEND_API_PRIVATE_URL_LIST="$CFG_BACKEND_API_PRIVATE_URL_LIST" \
